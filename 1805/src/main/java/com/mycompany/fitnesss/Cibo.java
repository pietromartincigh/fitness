public class Cibo {
    
    
    private String nome;
    
    private int kcal; //100g
    private int proteine;
    private int carboidrati;
    private int grassi;
    private int sodio;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getKcal() {
        return kcal;
    }

    public void setKcal(int kcal) {
        this.kcal = kcal;
    }

    public int getProteine() {
        return proteine;
    }

    public void setProteine(int proteine) {
        this.proteine = proteine;
    }

    public int getCarboidrati() {
        return carboidrati;
    }

    public void setCarboidrati(int carboidrati) {
        this.carboidrati = carboidrati;
    }

    public int getGrassi() {
        return grassi;
    }

    public void setGrassi(int grassi) {
        this.grassi = grassi;
    }

    public int getSodio() {
        return sodio;
    }

    public void setSodio(int sodio) {
        this.sodio = sodio;
    }

    
    
}
